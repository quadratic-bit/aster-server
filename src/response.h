#ifndef RESPONSE_H
#define RESPONSE_H

#include <stddef.h>

enum http_response_code {
	/* Informational 1xx */
	RC_100_CONTINUE = 100,
	RC_101_SWITCHING_PROTOCOLS = 101,

	/* Successful 2xx */
	RC_200_OK = 200,
	RC_201_CREATED = 201,
	RC_203_NON_AUTHORITATIVE_INFORMATION = 203,
	RC_204_NO_CONTENT = 204,
	RC_205_RESET_CONTENT = 205,
	RC_206_PARTIAL_CONTENT = 206,

	/* Redirection 3xx */
	RC_300_MULTIPLE_CHOICES = 300,
	RC_301_MOVED_PERMANENTLY = 301,
	RC_302_FOUND = 302,
	RC_303_SEE_OTHER = 303,
	RC_304_NOT_MODIFIED = 304,
	RC_305_USE_PROXY = 305,
	RC_307_TEMPORARY_REDIRECT = 307,

	/* Client Error 4xx */
	RC_400_BAD_REQUEST = 400,
	RC_401_UNAUTHORIZED = 401,
	RC_402_PAYMENT_REQUIRED = 402,
	RC_403_FORBIDDEN = 403,
	RC_404_NOT_FOUND = 404,
	RC_405_METHOD_NOT_ALLOWED = 405,
	RC_406_NOT_ACCEPTABLE = 406,
	RC_407_PROXY_AUTHENTICATION_REQUIRED = 407,
	RC_408_REQUEST_TIMEOUT = 408,
	RC_409_CONFLICT = 409,
	RC_410_GONE = 410,
	RC_411_LENGTH_REQUIRED = 411,
	RC_412_PRECONDITION_FAILED = 412,
	RC_413_REQUEST_ENTITY_TOO_LARGE = 413,
	RC_414_REQUEST_URI_TOO_LONG = 414,
	RC_415_UNSUPPORTED_MEDIA_TYPE = 415,
	RC_416_REQUESTED_RANGE_NOT_SATISFIABLE = 416,
	RC_417_EXPECTATION_FAILED = 417,

	/* Server Error 5xx */
	RC_500_INTERNAL_SERVER_ERROR = 500,
	RC_501_NOT_IMPLEMENTED = 501,
	RC_502_BAD_GATEWAY = 502,
	RC_503_SERVICE_UNAVAILABLE = 503,
	RC_504_GATEWAY_TIMEOUT = 504,
	RC_505_HTTP_VERSION_NOT_SUPPORTED = 505
};

struct http_response {
	char *buf;
	size_t len;
	size_t cap;
};

struct http_response new_response(void);
void append_to_response(struct http_response *resp, const char *str);
void http_response_free(struct http_response *resp);

#endif
